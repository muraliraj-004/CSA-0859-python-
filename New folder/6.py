#creating a list
list=['S','A','V','E','E','T','H','A']
print("initial list:")
print(list)

#print elements of a range using slice operation
sliced_list=list[3:8]
print(sliced_list)

#print elements from a pre-defined point to end
sliced_list=list[5:]
print(sliced_list)

#printing elements from beginning till end
sliced_list=list[:]
print(sliced_list)

